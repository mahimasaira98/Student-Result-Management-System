    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course=(String)jComboBox1.getSelectedItem();
        String branch=(String)jComboBox2.getSelectedItem();
        String rollno = jTextField7.getText();
        String name = jTextField8.getText();
        String fathername = jTextField9.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        try{
           Class.forName("com.mysql.cj.jdbc.Driver");
          Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Saira98*");
          Statement st = con.createStatement();
          if(rollno.equals(""))JOptionPane.showMessageDialog(null,"Roll number cannot be null");
          else {
              st.executeUpdate("insert into student values('"+rollno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fathername+"')");
              JOptionPane.showMessageDialog(null,"Successfully added a student");
                setVisible(false);
                new adminHome().setVisible(true);
          }
          
        }
        
        catch(HeadlessException | ClassNotFoundException | SQLException e){
          JOptionPane.showMessageDialog(null,e.toString());
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

