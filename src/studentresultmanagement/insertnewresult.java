    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String maths = jTextField2.getText();
        String mechanics = jTextField3.getText();
        String electrical = jTextField4.getText();
        String computer = jTextField5.getText();
        String lifeskill = jTextField6.getText();
       try{
           Class.forName("com.mysql.cj.jdbc.Driver");
          Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Saira98*");
          Statement st = con.createStatement();
          ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
          if(rs.next()){
             st.executeUpdate("insert into result(rollno, maths, mechanics, electrical, computer, lifeskill) values('"+rollno+"','"+maths+"','"+mechanics+"','"+electrical+"','"+computer+"','"+lifeskill+"')");
             JOptionPane.showMessageDialog(null,"Successfully updated result");
             setVisible(false);
             new insertnewresult().setVisible(true);
          }
          else{
              JOptionPane.showMessageDialog(null,"Roll no does not exist");
          }
        }
       
        catch(HeadlessException | ClassNotFoundException | SQLException e){
          JOptionPane.showMessageDialog(null,e.toString());
        }
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

